def gen_production_data():
    """生成工厂生产数据"""
    random.seed(42)
    lines = ["A线", "B线", "C线"]
    products = ["产品X-100", "产品Y-200", "产品Z-300"]
    records = []
    base_date = datetime(2026, 6, 1)
    for day in range(30):
        date = base_date + timedelta(days=day)
        for line in lines:
            for product in products:
                planned = random.randint(800, 1200)
                actual = int(planned * random.uniform(0.85, 1.02))
                defect = int(actual * random.uniform(0.005, 0.03))
                records.append({
                    "date": date.strftime("%Y-%m-%d"),
                    "line": line,
                    "product": product,
                    "planned_output": planned,
                    "actual_output": actual,
                    "defect_count": defect,
                    "defect_rate": round(defect / actual * 100, 2) if actual > 0 else 0,
                    "downtime_min": random.randint(0, 45)
                })
    ensure_data_dir()
    path = os.path.join(DATA_DIR, "production_data.json")
    with open(path, "w", encoding="utf-8") as f:
        json.dump(records, f, ensure_ascii=False, indent=2)
    print(f"[OK] 生产数据已生成: {path} ({len(records)}条)")
    return records


